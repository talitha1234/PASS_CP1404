# # 1b. Solution -  set oparations work with sets
my_shows = {'The Boys', 'Invincible', 'My Little Pony'}
your_shows = {'My Little Pony', 'Monster High', 'Power Puff Girls'}

# After fixing the above code, What will the following print?
print("union: ", my_shows | your_shows)
print("difference: ", my_shows - your_shows)
print("intersection: ", my_shows & your_shows)
print("symmetric difference: ", my_shows ^ your_shows)
